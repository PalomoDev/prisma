'use client'

import { useForm } from "react-hook-form";
import { zodResolver } from "@hookform/resolvers/zod";
import { updateFeature } from "@/lib/actions/spec-features.actions";
import { Form, FormControl, FormDescription, FormField, FormItem, FormLabel, FormMessage } from "@/components/ui/form";
import { Input } from "@/components/ui/input";
import { Button } from "@/components/ui/button";
import { Textarea } from "@/components/ui/textarea";
import { Checkbox } from "@/components/ui/checkbox";
import { useRouter } from "next/navigation";
import { UpdateFeatureInput } from "@/types";
import { updateFeatureSchema } from "@/lib/validations/category.validation";
import { useState } from "react";
import { toast } from "sonner";

interface UpdateFeatureFormProps {
    feature: any; // –¢–∏–ø–∏–∑–∏—Ä–æ–≤–∞—Ç—å –ø–æ–∑–∂–µ
}

const UpdateFeatureForm = ({ feature }: UpdateFeatureFormProps) => {
    const [serverError, setServerError] = useState<string>('');
    const router = useRouter();

    // –û–ø—Ä–µ–¥–µ–ª—è–µ–º –∑–Ω–∞—á–µ–Ω–∏—è –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é –∏–∑ –ø–µ—Ä–µ–¥–∞–Ω–Ω–æ–π –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏
    const defaultValues: UpdateFeatureInput = {
        id: feature.id,
        name: feature.name,
        key: feature.key,
        icon: feature.icon,
        iconImage: feature.iconImage || null,
        description: feature.description || null,
        isActive: feature.isActive,
        sortOrder: feature.sortOrder,
    };

    const form = useForm<UpdateFeatureInput>({
        resolver: zodResolver(updateFeatureSchema),
        defaultValues,
        mode: 'onChange'
    });

    const handleNameChange = (value: string) => {
        // –ì–µ–Ω–µ—Ä–∞—Ü–∏—è key —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –ø–æ–ª–µ key –ø—É—Å—Ç–æ–µ –∏–ª–∏ —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Å –∞–≤—Ç–æ–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–º
        const currentKey = form.getValues('key');
        const autoGeneratedKey = feature.name
            .toLowerCase()
            .replace(/[^a-zA-Z0-9\s]/g, '')
            .trim()
            .replace(/\s+/g, '-');

        // –û–±–Ω–æ–≤–ª—è–µ–º key —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –æ–Ω –±—ã–ª –∞–≤—Ç–æ–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –∏–ª–∏ –ø—É—Å—Ç–æ–π
        if (!currentKey || currentKey === autoGeneratedKey) {
            const generatedKey = value
                .toLowerCase()
                .replace(/[^a-zA-Z0-9\s]/g, '')
                .trim()
                .replace(/\s+/g, '-');

            form.setValue('key', generatedKey, {
                shouldValidate: true,
                shouldDirty: true
            });
        }

        // –û–±–Ω–æ–≤–ª—è–µ–º icon (–Ω–∞–∑–≤–∞–Ω–∏–µ –∫–∞–∫ –µ—Å—Ç—å)
        form.setValue('icon', value, {
            shouldValidate: true,
            shouldDirty: true
        });
    };

    const handleSubmit = async (values: UpdateFeatureInput) => {
        try {
            setServerError('');
            console.log('üöÄ Form submitting with values:', values);

            const res = await updateFeature(values);
            console.log('üìù Response from server:', res);

            if (res.success) {
                toast.success(res.message || '–û—Å–æ–±–µ–Ω–Ω–æ—Å—Ç—å —É—Å–ø–µ—à–Ω–æ –æ–±–Ω–æ–≤–ª–µ–Ω–∞');
                router.push('/admin/products/specifications');
            } else {
                setServerError(res.message || '–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏');
                toast.error(res.message || '–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏');
            }
        } catch (error) {
            console.error('‚ùå Client error updating feature:', error);
            const errorMessage = '–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏';
            setServerError(errorMessage);
            toast.error(errorMessage);
        }
    };

    const handleCancelForm = () => {
        router.push('/admin/products/specifications');
    };

    return (
        <div className="mx-auto py-6">
            <div className="mb-6">
                <h1 className="text-2xl font-bold">–†–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞—Ç—å –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç—å</h1>
                <p className="text-muted-foreground">–û–±–Ω–æ–≤–∏—Ç–µ –¥–∞–Ω–Ω—ã–µ –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏ —Ç–æ–≤–∞—Ä–∞</p>
            </div>

            <Form {...form}>
                <form
                    onSubmit={form.handleSubmit(handleSubmit)}
                    className='space-y-8'
                    noValidate
                >
                    {/* –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –æ—à–∏–±–∫—É —Å–µ—Ä–≤–µ—Ä–∞ */}
                    {serverError && (
                        <div className="p-4 mb-4 text-sm text-red-800 rounded-lg bg-red-50 border border-red-200">
                            {serverError}
                        </div>
                    )}

                    {/* Hidden ID field */}
                    <FormField
                        control={form.control}
                        name='id'
                        render={({ field }) => (
                            <Input type="hidden" {...field} />
                        )}
                    />

                    {/* Name */}
                    <FormField
                        control={form.control}
                        name='name'
                        render={({ field }) => (
                            <FormItem>
                                <FormLabel>
                                    –ù–∞–∑–≤–∞–Ω–∏–µ –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏ <span className="text-red-500">*</span>
                                </FormLabel>
                                <FormControl>
                                    <Input
                                        placeholder='–í–≤–µ–¥–∏—Ç–µ –Ω–∞–∑–≤–∞–Ω–∏–µ –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏'
                                        {...field}
                                        onChange={(e) => {
                                            field.onChange(e);
                                            handleNameChange(e.target.value);
                                        }}
                                    />
                                </FormControl>
                                <FormDescription>
                                    –ù–∞–ø—Ä–∏–º–µ—Ä: "–í–æ–¥–æ—Å—Ç–æ–π–∫–∏–π", "–õ–µ–≥–∫–∏–π", "–í–µ—Ç—Ä–æ–∑–∞—â–∏—Ç–Ω—ã–π"
                                </FormDescription>
                                <FormMessage />
                            </FormItem>
                        )}
                    />

                    {/* Key and Icon (read-only, auto-generated) */}
                    <div className='flex flex-col md:flex-row gap-6 items-start'>
                        <FormField
                            control={form.control}
                            name='key'
                            render={({ field }) => (
                                <FormItem className='flex-1'>
                                    <FormLabel>
                                        –ö–ª—é—á <span className="text-red-500">*</span>
                                    </FormLabel>
                                    <FormControl>
                                        <Input
                                            placeholder='–ö–ª—é—á –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏'
                                            {...field}
                                        />
                                    </FormControl>
                                    <FormDescription>
                                        –£–Ω–∏–∫–∞–ª—å–Ω—ã–π –∫–ª—é—á –¥–ª—è –ø—Ä–æ–≥—Ä–∞–º–º–Ω–æ–≥–æ –¥–æ—Å—Ç—É–ø–∞
                                    </FormDescription>
                                    <FormMessage />
                                </FormItem>
                            )}
                        />

                        <FormField
                            control={form.control}
                            name='icon'
                            render={({ field }) => (
                                <FormItem className='flex-1'>
                                    <FormLabel>
                                        –ò–∫–æ–Ω–∫–∞ <span className="text-red-500">*</span>
                                    </FormLabel>
                                    <FormControl>
                                        <Input
                                            placeholder='–ù–∞–∑–≤–∞–Ω–∏–µ –∏–∫–æ–Ω–∫–∏'
                                            {...field}
                                        />
                                    </FormControl>
                                    <FormDescription>
                                        –ù–∞–∑–≤–∞–Ω–∏–µ –∏–∫–æ–Ω–∫–∏ (–æ–±—ã—á–Ω–æ —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Å –Ω–∞–∑–≤–∞–Ω–∏–µ–º)
                                    </FormDescription>
                                    <FormMessage />
                                </FormItem>
                            )}
                        />
                    </div>

                    {/* Description */}
                    <FormField
                        control={form.control}
                        name='description'
                        render={({ field }) => (
                            <FormItem>
                                <FormLabel>
                                    –û–ø–∏—Å–∞–Ω–∏–µ
                                </FormLabel>
                                <FormControl>
                                    <Textarea
                                        placeholder='–í–≤–µ–¥–∏—Ç–µ –æ–ø–∏—Å–∞–Ω–∏–µ –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏ (–Ω–µ–æ–±—è–∑–∞—Ç–µ–ª—å–Ω–æ)'
                                        className='resize-none min-h-24'
                                        {...field}
                                        value={field.value || ''}
                                    />
                                </FormControl>
                                <FormDescription>
                                    –ü–æ–¥—Ä–æ–±–Ω–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏ –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
                                </FormDescription>
                                <FormMessage />
                            </FormItem>
                        )}
                    />

                    {/* Icon Image URL */}
                    <FormField
                        control={form.control}
                        name='iconImage'
                        render={({ field }) => (
                            <FormItem>
                                <FormLabel>
                                    URL –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –∏–∫–æ–Ω–∫–∏
                                </FormLabel>
                                <FormControl>
                                    <Input
                                        placeholder='–í–≤–µ–¥–∏—Ç–µ URL –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –∏–∫–æ–Ω–∫–∏ (–Ω–µ–æ–±—è–∑–∞—Ç–µ–ª—å–Ω–æ)'
                                        {...field}
                                        value={field.value || ''}
                                    />
                                </FormControl>
                                <FormDescription>
                                    –ê–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω–æ–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –≤–º–µ—Å—Ç–æ —Ç–µ–∫—Å—Ç–æ–≤–æ–π –∏–∫–æ–Ω–∫–∏
                                </FormDescription>
                                <FormMessage />
                            </FormItem>
                        )}
                    />

                    {/* Sort Order */}
                    <FormField
                        control={form.control}
                        name='sortOrder'
                        render={({ field }) => (
                            <FormItem className='max-w-xs'>
                                <FormLabel>
                                    –ü–æ—Ä—è–¥–æ–∫ —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∏ <span className="text-red-500">*</span>
                                </FormLabel>
                                <FormControl>
                                    <Input
                                        type='number'
                                        placeholder='0'
                                        {...field}
                                        onChange={(e) => field.onChange(e.target.value ? parseInt(e.target.value) : 0)}
                                    />
                                </FormControl>
                                <FormDescription>
                                    –ú–µ–Ω—å—à–∏–µ —á–∏—Å–ª–∞ –æ—Ç–æ–±—Ä–∞–∂–∞—é—Ç—Å—è –ø–µ—Ä–≤—ã–º–∏. –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ 0 –¥–ª—è –≤—ã—Å—à–µ–≥–æ –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç–∞.
                                </FormDescription>
                                <FormMessage />
                            </FormItem>
                        )}
                    />

                    {/* Is Active Checkbox */}
                    <FormField
                        control={form.control}
                        name='isActive'
                        render={({ field }) => (
                            <FormItem className='flex flex-row items-start space-x-3 space-y-0'>
                                <FormControl>
                                    <Checkbox
                                        checked={field.value}
                                        onCheckedChange={field.onChange}
                                        aria-label="–ê–∫—Ç–∏–≤–Ω–∞—è –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç—å"
                                    />
                                </FormControl>
                                <div className='space-y-1 leading-none'>
                                    <FormLabel>
                                        –ê–∫—Ç–∏–≤–Ω–∞—è –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç—å
                                    </FormLabel>
                                    <FormDescription>
                                        –≠—Ç–∞ –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç—å –±—É–¥–µ—Ç –¥–æ—Å—Ç—É–ø–Ω–∞ –¥–ª—è –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –≤ —Ç–æ–≤–∞—Ä–∞—Ö
                                    </FormDescription>
                                </div>
                                <FormMessage />
                            </FormItem>
                        )}
                    />

                    {/* Submit Button */}
                    <div className='flex gap-4'>
                        <Button
                            type='button'
                            variant='outline'
                            size='lg'
                            className='flex-1'
                            onClick={handleCancelForm}
                            disabled={form.formState.isSubmitting}
                        >
                            –û—Ç–º–µ–Ω–∞
                        </Button>
                        <Button
                            type='submit'
                            size='lg'
                            disabled={form.formState.isSubmitting}
                            className='flex-1'
                        >
                            {form.formState.isSubmitting ? '–û–±–Ω–æ–≤–ª–µ–Ω–∏–µ...' : '–û–±–Ω–æ–≤–∏—Ç—å –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç—å'}
                        </Button>
                    </div>
                </form>
            </Form>
        </div>
    );
};

export default UpdateFeatureForm;